public class Incrementoperators {

	public static void main(String[] args) {
    int a =10;
//     a=a+1;
//    a++;
//  a = a-1;
 //  a--;
    
    System.out.println(a);
    
//   post increment
     a=60;   // data type is declared at the start of the programming
    int res=a++;
    System.out.println(res);
    
//    preincrement
    a=70;
    int addpre=++a;
    System.out.println(addpre);
     
	}

}
